    private void popula() {
        
        Connection con = ConnectionFactory.createConnectionToMySQL();
        
        ProdutoDAO dao = new ProdutoDAO(con);
        tbm.addList(dao.findALL());
        
        CategoriaDAO daoCat = new CategoriaDAO(con);
        cbm.addAll(daoCat.findALL());

    }

